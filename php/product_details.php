    function generateBreadcrumb()
    {
        $breadcrumb = '<a href="/">Home</a>';

        // Get the current page URL
        $url = $_SERVER['REQUEST_URI'];
        $urlParts = explode('/', $url);

        $path = '/';
        foreach ($urlParts as $part) {
            if ($part !== '') {
                $path .= $part . '/';
                $breadcrumb .= ' / <a href="' . $path . '">' . ucfirst($part) . '</a>';
            }
        }

        return $breadcrumb;
    }
